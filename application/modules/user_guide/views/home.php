Bdeal.in is an online shopping mall portal which enables vendors to set up online shops, customers to browse through the shops , and a system administrator to approve and reject requests for new shops and maintain lists of shop categories.

Owners can manage the items in the shop and also help customers purchase them online without having to visit the shop physically.

This online shopping mall will use the internet as the sole method for selling goods to its customers. The consumer will be in complete control of his/her shopping experience by using the “unique storefront” concept. Shopping will be highly personalized and the mall will provide lower prices than most competitors.  
 

<p>
1.	<b>Mall Administrator:</b> The Mall Administrator is the super user and has complete control over all the activities that can be performed. The application notifies the administrator of all shop creation requests, and the administrator can then approve or reject them. The administrator also manages the list of available product categories. The administrator can also view and delete entries in the guestbook.</p>

<p>2.	 <b>Shop Owner:</b> Any user can submit a shop creation request through the application. When the request is approved by the Mall Administrator, the requester is notified, and from there on is given the role of Shop Owner. The Shop Owner is responsible for setting up the shop and maintaining it. The job involves managing the sub-categories of the items in the shop. Also, the shop owner can add or remove items from his shop. The Shop Owner can view different reports that give details of the sales and orders specific to his shop. The Shop Owner can also decide to close shop and remove it from the mall.
</p>
<p>
3.	<b>Mall Customer/Guests:</b> A Mall Customer can browse through the shops and choose products to place in a virtual shopping cart or the wishlist. The shopping cart details can be viewed and items can be removed from the cart. To proceed with the purchase, the customer is prompted to login. Also, the customer can modify personal profile information (such as phone number and shipping address) stored by the application. The customer can also view the status of any previous orders, and cancel any order that has not been shipped yet.
</p><p>
4.	 <b>Employees:</b>

<p>•	Purchase department under a Purchase manager to overlook purchasing activities if warehousing needs arise.</p>
<p>•	Sales department under a Sales manager who will look after the sale of products and services, the most important activity.</p>
<p>•	Accounts department under an Accounts manager to look after the accounting activities of the enterprise.</p>


